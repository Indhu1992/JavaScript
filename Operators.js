console.log(++x)
// decrement by 1 
console.log(--x)

// comparison operators -> boolean values > these operators will use in loops and matching statements
console.log(x==y)
// Its a strict equality operator
console.log(x===y)
console.log(x!==y)
console.log(x>y)
console.log(x>=y)
console.log(x<y)
console.log(x<=y)
